#include <unistd.h>

void	ft_is_negative(int n)
{
	char	p;
	char	v;

	p = 'P';
	v = 'N';
	if (n < 0)
	{
		write(1, &v, 1);
	}
	else
	{
		write(1, &p, 1);
	}
}
